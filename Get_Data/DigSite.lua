function WoWTools_ChineseMixin:GetDigSite(digSiteID)
    digSiteID= digSiteID and tonumber(digSiteID)
    if digSiteID then
        local cn= tab[digSiteID]
        if self:IsCN(cn) then
            return cn
        end
    end
end


hooksecurefunc(DigSitePinMixin, 'OnMouseEnter', function(self)
    local name= self.poiInfo and WoWTools_ChineseMixin:GetDigSite(self.poiInfo.researchSiteID) or WoWTools_ChineseMixin:SetText(self.poiInfo.name)
    if name then
        local desc= nil
        self:GetMap():TriggerEvent("SetAreaLabel", MAP_AREA_LABEL_TYPE.POI, name , desc)
    end
end)